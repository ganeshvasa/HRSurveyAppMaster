package com.bbyn.rest.model;

public class SurveyMaster {
	
	private String surveyCode;
	private String surveryName;
	private String surveryType;
	private String status;
	private String targetDeptName;
	private String targetGroupName;
	private String createdDate;
	private String approvedDate;
	public String getSurveyCode() {
		return surveyCode;
	}
	public void setSurveyCode(String surveyCode) {
		this.surveyCode = surveyCode;
	}
	public String getSurveryName() {
		return surveryName;
	}
	public void setSurveryName(String surveryName) {
		this.surveryName = surveryName;
	}
	public String getSurveryType() {
		return surveryType;
	}
	public void setSurveryType(String surveryType) {
		this.surveryType = surveryType;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getTargetDeptName() {
		return targetDeptName;
	}
	public void setTargetDeptName(String targetDeptName) {
		this.targetDeptName = targetDeptName;
	}
	public String getTargetGroupName() {
		return targetGroupName;
	}
	public void setTargetGroupName(String targetGroupName) {
		this.targetGroupName = targetGroupName;
	}
	public String getCreatedDate() {
		return createdDate;
	}
	public void setCreatedDate(String createdDate) {
		this.createdDate = createdDate;
	}
	public String getApprovedDate() {
		return approvedDate;
	}
	public void setApprovedDate(String approvedDate) {
		this.approvedDate = approvedDate;
	}
	
	

}
